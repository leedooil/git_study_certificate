# 반복문(while, for)

'''
for 변수명 in range()   범위 지정함수

for 변수명 in 열거형변수(데이터 타입) 

'''

for i in range(10):     # 0 ~ 10전까지 1씩 증가
    print(i,end = '\t')
    
for i in range(1,10):   # 1 ~ 10전까지 1씩 증가
    print(i,end = '\t')
        
for i in range(1,10,2): # 1 ~ 10전까지 2씩 증가
    print(i,end = '\t')
    
