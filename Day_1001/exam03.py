'''
 ##연산자##

 산술(*, %, /:무조건 실수나옴, //:몫)

 관계(<, <=, >, >=, ==, !=)

 논리(and, or, not)

 (++, --) : Python에선 없음
 
'''

print("7 / 5 = ", 7 / 5)     # 실수
print("7 % 5 = ", 7 % 5)     # 나머지
print("7 // 5 = ", 7 // 5)   # 몫 
print("7 > 5 = ", 7 > 5)     # 관계연산자(True, False)

print("2 ** 3 = ", 2**3)     # 2^3
