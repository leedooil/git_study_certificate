
public class Exam_08 {

	public static void main(String[] args) {
		// 반복횟수 정해져있음   : for문 유리
 		// 반복횟수 안정해져있음 : while문 유리
		int i;
		for (i = 1; i <= 5; i++) {
//			System.out.println(i);
		}
		System.out.println(i);
		System.out.println("--------------");
		for (int j = 5; j < i; j--) {
			System.out.println(j);
		}
	}

}
