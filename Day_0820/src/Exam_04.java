
public class Exam_04 {
	
	public static void main(String[] args) {
		System.out.print("*");
		System.out.print("**");
		System.out.print("***");
		System.out.println();

		// escape문자 \n : 줄바꿈
		System.out.print("*\n");
		System.out.print("**\n");
		System.out.print("***\n");
		
		// escape문자 \t : tap 역할 오른쪽으로 8번 스페이스바
		System.out.println("번호	이름	국어");
		System.out.println("번호\t이름\t국어");
		
		System.out.print("\n");
		System.out.println();
	}
}
